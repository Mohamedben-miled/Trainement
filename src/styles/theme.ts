// Helper function to get a theme value
export const getThemeValue = (path: string, defaultValue?: any) => {
  const keys = path.split('.');
  let value = theme;
  
  for (const key of keys) {
    if (value && typeof value === 'object' && key in value) {
      value = value[key];
    } else {
      return defaultValue;
    }
  }
  
  return value || defaultValue;
};

